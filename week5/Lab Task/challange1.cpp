#include <iostream>
using namespace std;

int main()
{
    cout << "Multiplication Table of 24:" << endl;
    for (int i = 1; i <= 10; i++) {
        cout << "24 x " << i << " = " << 24 * i << endl;
    }
    
    cout << endl;

    cout << "Multiplication Table of 50:" << endl;
    for (int i = 1; i <= 10; i++) {
        cout << "50 x " << i << " = " << 50 * i << endl;
    }

    cout << endl;
    
    
    
    return 0; 
} 